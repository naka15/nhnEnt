package com.twoyears.model.dto;

import java.util.Date;

public class boardMainViewDTO {

	private int noticeNo;
    private Date noticeDate;
	private String bookSubject;
	private String bookPic;
	
	public boardMainViewDTO() {
		
	}

	public boardMainViewDTO(int noticeNo, Date noticeDate, String bookSubject, String bookPic) {
		super();
		this.noticeNo = noticeNo;
		this.noticeDate = noticeDate;
		this.bookSubject = bookSubject;
		this.bookPic = bookPic;
	}

	public int getNoticeNo() {
		return noticeNo;
	}

	public void setNoticeNo(int noticeNo) {
		this.noticeNo = noticeNo;
	}
	
	public Date getNoticeDate() {
		return noticeDate;
	}

	public void setNoticeDate(Date noticeDate) {
		this.noticeDate = noticeDate;
	}

	public String getBookSubject() {
		return bookSubject;
	}

	public void setBookSubject(String bookSubject) {
		this.bookSubject = bookSubject;
	}

	public String getBookPic() {
		return bookPic;
	}

	public void setBookPic(String bookPic) {
		this.bookPic = bookPic;
	}

	@Override
	public String toString() {
		return "mainViewDTO [noticeDate=" + noticeDate + ", bookSubject="
				+ bookSubject + ", bookPic=" + bookPic + "]";
	}
	
		
	
}

package com.twoyears.model.dto;

public class tradeDTO {
   private String receiverUserId;
   private String receiverUserNick;
   private int noticeNo;
   private String tradeDate;
   private String senderUserNick;

   private int senderStatus;
   private int receiverStatus;
   
   private NoticeDTO notice;      //트레이드 게시물
   private String tradeStatus;      //진행상황

   
   public String getSenderUserNick() {
      return senderUserNick;
   }
   public void setSenderUserNick(String senderUserNick) {
      this.senderUserNick = senderUserNick;
   }

   
   public String getReceiverUserNick() {
      return receiverUserNick;
   }
   public void setReceiverUserNick(String receiverUserNick) {
      this.receiverUserNick = receiverUserNick;
   }

   
   public NoticeDTO getNotice() {
      return notice;
   }
   public void setNotice(NoticeDTO notice) {
      this.notice = notice;
   }
   public String getTradeStatus() {
      return tradeStatus;
   }
   public void setTradeStatus(String tradeStatus) {
      this.tradeStatus = tradeStatus;
   }
   public String getReceiverUserId() {
      return receiverUserId;
   }
   public void setReceiverUserId(String receiverUserId) {
      this.receiverUserId = receiverUserId;
   }
   public int getNoticeNo() {
      return noticeNo;
   }
   public void setNoticeNo(int noticeNo) {
      this.noticeNo = noticeNo;
   }
   public String getTradeDate() {
      return tradeDate;
   }
   public void setTradeDate(String tradeDate) {
      this.tradeDate = tradeDate;
   }
   public int getSenderStatus() {
      return senderStatus;
   }
   public void setSenderStatus(int senderStatus) {
      this.senderStatus = senderStatus;
   }
   public int getReceiverStatus() {
      return receiverStatus;
   }
   public void setReceiverStatus(int receiverStatus) {
      this.receiverStatus = receiverStatus;
   }
   
   @Override
   public String toString() {
      return "tradeDTO [receiverUserId=" + receiverUserId + ", noticeNo="
            + noticeNo + ", tradeDate=" + tradeDate + ", senderStatus="
            + senderStatus + ", receiverStatus=" + receiverStatus
            + ", notice=" + notice + ", tradeStatus=" + tradeStatus + "]";
   }
}